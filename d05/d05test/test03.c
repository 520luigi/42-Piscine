#include <unistd.h>
#include <stdio.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

char	*ft_strcpy(char *dest, char *src)
{
	int index;

	index = 0;
	while (src[index] != '\0')
	{
		dest[index] = src[index];
		index++;
	}

//=========
int inde = 0 ;
while (dest[inde] != '\0')
{
	ft_putchar(dest[inde]);
	inde++;
}


//=======

	return (dest);
}


